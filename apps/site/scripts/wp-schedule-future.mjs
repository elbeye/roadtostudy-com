// Scheduled-publishing parity (§6 kabul listesi): WordPress 'future' posts must keep
// publishing automatically on the target. The seed pipeline lands them as plain
// drafts (EmDash's seed apply has no scheduled path), so this post-import step sets
// status='scheduled' + scheduled_at from the source publish datetime — the worker's
// per-minute sweep (findReadyToPublish) then publishes each one on time. Source of
// truth for WHICH posts were 'future' (and when) is the WP export, not the DB.
//
// Usage:
//   node scripts/wp-schedule-future.mjs                  # writes data/schedule-future.sql
//   WP_SQLITE_INPUT=./data.db node scripts/wp-schedule-future.mjs --apply-local
//
// The generated SQL is idempotent and guarded: it only touches rows still in
// status='draft', so re-runs and editor changes are never clobbered. Apply to prod:
//   npx wrangler d1 execute roadtostudy-emdash-poc --remote -y --file data/schedule-future.sql
import { readFile, writeFile } from "node:fs/promises";

const inputPath = process.env.WP_SAMPLE_INPUT || "data/wp-full.json";
const outputPath = process.env.WP_SCHEDULE_SQL_OUTPUT || "data/schedule-future.sql";

const source = JSON.parse(await readFile(inputPath, "utf8"));
const posts = source.content?.posts || [];

// WP REST `date_gmt` is the canonical scheduled publish time for future posts.
// Matching is by slug alone — slugs are globally unique across ec_posts (WordPress
// de-duplicates them across locales; verified 3766/3766 distinct).
export function futureSchedule(posts) {
	return posts
		.filter((p) => p.status === "future" && p.slug && p.date_gmt)
		.map((p) => ({ slug: p.slug, at: `${p.date_gmt.replace(" ", "T")}${p.date_gmt.endsWith("Z") ? "" : "Z"}` }));
}

const sq = (v) => `'${String(v).replaceAll("'", "''")}'`;

const rows = futureSchedule(posts);
const stmts = rows.map(
	(r) =>
		`UPDATE ec_posts SET status = 'scheduled', scheduled_at = ${sq(r.at)} WHERE slug = ${sq(r.slug)} AND status = 'draft';`,
);
await writeFile(outputPath, `-- Generated by scripts/wp-schedule-future.mjs (${rows.length} rules)\n${stmts.join("\n")}\n`);
console.log(JSON.stringify({ inputPath, outputPath, futurePosts: rows.length, earliest: rows.map((r) => r.at).sort()[0], latest: rows.map((r) => r.at).sort().at(-1) }, null, 2));

if (process.argv.includes("--apply-local")) {
	const { default: Database } = await import("better-sqlite3");
	const db = new Database(process.env.WP_SQLITE_INPUT || "./data.db");
	const update = db.prepare("UPDATE ec_posts SET status = 'scheduled', scheduled_at = ? WHERE slug = ? AND status = 'draft'");
	let changed = 0;
	for (const r of rows) changed += update.run(r.at, r.slug).changes;
	console.log(`local: ${changed} rows scheduled`);
}
