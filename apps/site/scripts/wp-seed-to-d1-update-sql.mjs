import { mkdir, rm, writeFile } from "node:fs/promises";
import { join } from "node:path";

const inputPath = process.env.WP_SEED_INPUT || "seed/seed.json";
const outputDir = process.env.WP_D1_UPDATE_SQL_DIR || "data/d1-update-sql";
const maxChunkBytes = Math.max(1024 * 1024, Number(process.env.WP_D1_SQL_MAX_CHUNK_BYTES || 2 * 1024 * 1024));

const seed = JSON.parse(await readFileText(inputPath));
const updates = [];

for (const entry of seed.content?.posts || []) {
	if (entry.status !== "published") continue;
	const data = entry.data || {};
	const sets = commonSets(data);
	const featuredImage = data.featured_image ? { ...data.featured_image, provider: "external" } : null;
	if (featuredImage) sets.push(`"featured_image" = ${sqlLiteral(JSON.stringify(featuredImage))}`);
	if (sets.length === 0) continue;
	updates.push(updateSql("ec_posts", entry, sets));
	updates.push(revisionUpdateSql("ec_posts", "posts", entry, data, featuredImage));
}

for (const entry of seed.content?.pages || []) {
	if (entry.status !== "published") continue;
	const data = entry.data || {};
	const sets = commonSets(data);
	if (sets.length === 0) continue;
	updates.push(updateSql("ec_pages", entry, sets));
	updates.push(revisionUpdateSql("ec_pages", "pages", entry, data));
}

await rm(outputDir, { recursive: true, force: true });
await mkdir(outputDir, { recursive: true });

const chunks = chunkStatements(updates, maxChunkBytes);
const files = [];
for (let i = 0; i < chunks.length; i++) {
	const sql = [
		`-- Generated by scripts/wp-seed-to-d1-update-sql.mjs (${i + 1}/${chunks.length})`,
		...chunks[i],
		"",
	].join("\n");
	const file = `${String(i + 1).padStart(3, "0")}-update-content-seo.sql`;
	await writeFile(join(outputDir, file), sql);
	files.push({ file, statements: chunks[i].length, bytes: Buffer.byteLength(sql) });
}

console.log(JSON.stringify({ inputPath, outputDir, maxChunkBytes, statements: updates.length, files }, null, 2));

function commonSets(data) {
	const sets = [];
	if (data.source_seo) sets.push(`"source_seo" = ${sqlLiteral(JSON.stringify(data.source_seo))}`);
	if (data.content_html) sets.push(`"content_html" = ${sqlLiteral(data.content_html)}`);
	if (data.wp_published_at) sets.push(`"wp_published_at" = ${sqlLiteral(data.wp_published_at)}`);
	if (data.wp_modified_at) sets.push(`"wp_modified_at" = ${sqlLiteral(data.wp_modified_at)}`);
	return sets;
}

function updateSql(table, entry, sets) {
	const locale = entry.locale || "tr";
	return [
		`UPDATE ${quoteIdent(table)}`,
		`SET ${sets.join(", ")}`,
		`WHERE "deleted_at" IS NULL AND "status" = 'published' AND "locale" = ${sqlLiteral(locale)} AND "slug" = ${sqlLiteral(entry.slug)};`,
	].join(" ");
}

function revisionUpdateSql(table, collection, entry, data, featuredImage = null) {
	const locale = entry.locale || "tr";
	const args = [];
	if (data.source_seo) args.push("'$.source_seo'", `json(${sqlLiteral(JSON.stringify(data.source_seo))})`);
	if (data.content_html) args.push("'$.content_html'", sqlLiteral(data.content_html));
	if (data.wp_published_at) args.push("'$.wp_published_at'", sqlLiteral(data.wp_published_at));
	if (data.wp_modified_at) args.push("'$.wp_modified_at'", sqlLiteral(data.wp_modified_at));
	if (featuredImage) args.push("'$.featured_image'", `json(${sqlLiteral(JSON.stringify(featuredImage))})`);
	return [
		`UPDATE "revisions"`,
		`SET "data" = json_set("data", ${args.join(", ")})`,
		`WHERE "collection" = ${sqlLiteral(collection)} AND "id" = (`,
		`SELECT "live_revision_id" FROM ${quoteIdent(table)}`,
		`WHERE "deleted_at" IS NULL AND "status" = 'published' AND "locale" = ${sqlLiteral(locale)} AND "slug" = ${sqlLiteral(entry.slug)}`,
		`);`,
	].join(" ");
}

function quoteIdent(name) {
	return `"${String(name).replaceAll('"', '""')}"`;
}

function sqlLiteral(value) {
	if (value === null || value === undefined) return "NULL";
	if (typeof value === "number") {
		if (!Number.isFinite(value)) throw new Error(`Cannot serialize non-finite number: ${value}`);
		return String(value);
	}
	return `'${String(value).replaceAll("'", "''")}'`;
}

function chunkStatements(statements, maxBytes) {
	const chunks = [];
	let current = [];
	let bytes = 0;
	for (const statement of statements) {
		const statementBytes = Buffer.byteLength(statement) + 1;
		if (current.length > 0 && bytes + statementBytes > maxBytes) {
			chunks.push(current);
			current = [];
			bytes = 0;
		}
		current.push(statement);
		bytes += statementBytes;
	}
	if (current.length > 0) chunks.push(current);
	return chunks;
}

async function readFileText(path) {
	const { readFile } = await import("node:fs/promises");
	return readFile(path, "utf8");
}
