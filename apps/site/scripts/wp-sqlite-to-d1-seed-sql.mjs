import { mkdir, rm, writeFile } from "node:fs/promises";
import { join } from "node:path";
import Database from "better-sqlite3";

const inputDb = process.env.WP_SQLITE_INPUT || "/private/tmp/wp-full-import-test-update.db";
const outputDir = process.env.WP_D1_SEED_SQL_DIR || "data/d1-seed-sql";
const maxChunkBytes = Math.max(1024 * 1024, Number(process.env.WP_D1_SQL_MAX_CHUNK_BYTES || 5 * 1024 * 1024));

const deleteOrder = [
	"content_taxonomies",
	"ec_posts",
	"ec_pages",
	"revisions",
	"taxonomies",
	"_emdash_taxonomy_defs",
	"_emdash_menu_items",
	"_emdash_menus",
	"_emdash_bylines",
	"_emdash_fields",
	"_emdash_collections",
	"options",
];

const insertOrder = [
	"options",
	"_emdash_collections",
	"_emdash_fields",
	"_emdash_taxonomy_defs",
	"taxonomies",
	"_emdash_bylines",
	"_emdash_menus",
	"_emdash_menu_items",
	"revisions",
	"ec_posts",
	"ec_pages",
	"content_taxonomies",
];

await rm(outputDir, { recursive: true, force: true });
await mkdir(outputDir, { recursive: true });

const db = new Database(inputDb, { readonly: true, fileMustExist: true });
const files = [];
const deleteSql = [
	"-- Generated by scripts/wp-sqlite-to-d1-seed-sql.mjs",
	"-- Deletes only migrated seed/schema/content rows; migrations, auth, users, FTS shadows, and plugin tables are left alone.",
	...deleteOrder.map((table) => `DELETE FROM ${quoteIdent(table)};`),
	"",
].join("\n");
await writeFile(join(outputDir, "000-delete-seed-data.sql"), deleteSql);
files.push({ file: "000-delete-seed-data.sql", statements: deleteOrder.length });

let fileIndex = 1;
for (const table of insertOrder) {
	const inserts = dumpInserts(table);
	const chunks = chunkStatements(inserts, maxChunkBytes);
	let chunkIndex = 1;
	for (const chunk of chunks) {
		const sql = [
			`-- Inserts for ${table}${chunks.length > 1 ? ` (chunk ${chunkIndex}/${chunks.length})` : ""}`,
			...chunk,
			"",
		].join("\n");
		const base = `${String(fileIndex).padStart(3, "0")}-${table.replace(/^_+/, "").replaceAll("_", "-")}`;
		const file = chunks.length > 1 ? `${base}-${String(chunkIndex).padStart(3, "0")}.sql` : `${base}.sql`;
		await writeFile(join(outputDir, file), sql);
		files.push({ file, table, chunk: chunkIndex, chunks: chunks.length, statements: chunk.length, bytes: Buffer.byteLength(sql) });
		chunkIndex++;
	}
	fileIndex++;
}

console.log(JSON.stringify({ inputDb, outputDir, maxChunkBytes, files }, null, 2));

function dumpInserts(table) {
	const columns = db.prepare(`PRAGMA table_info(${quoteIdent(table)})`).all().map((column) => column.name);
	if (columns.length === 0) throw new Error(`Table not found or has no columns: ${table}`);

	const columnSql = columns.map(quoteIdent).join(", ");
	return db
		.prepare(`SELECT ${columnSql} FROM ${quoteIdent(table)} ORDER BY rowid`)
		.all()
		.map((row) => {
			const values = columns.map((column) => sqlLiteral(row[column])).join(", ");
			return `INSERT INTO ${quoteIdent(table)} (${columnSql}) VALUES (${values});`;
		});
}

function quoteIdent(name) {
	return `"${String(name).replaceAll('"', '""')}"`;
}

function chunkStatements(statements, maxBytes) {
	const chunks = [];
	let current = [];
	let bytes = 0;
	for (const statement of statements) {
		const statementBytes = Buffer.byteLength(statement) + 1;
		if (current.length > 0 && bytes + statementBytes > maxBytes) {
			chunks.push(current);
			current = [];
			bytes = 0;
		}
		current.push(statement);
		bytes += statementBytes;
	}
	if (current.length > 0) chunks.push(current);
	return chunks;
}

function sqlLiteral(value) {
	if (value === null || value === undefined) return "NULL";
	if (typeof value === "number") {
		if (!Number.isFinite(value)) throw new Error(`Cannot serialize non-finite number: ${value}`);
		return String(value);
	}
	if (typeof value === "bigint") return String(value);
	if (Buffer.isBuffer(value)) return `X'${value.toString("hex")}'`;
	return `'${String(value).replaceAll("'", "''")}'`;
}
